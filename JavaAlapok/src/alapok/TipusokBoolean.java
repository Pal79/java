package alapok;

public class TipusokBoolean {
	
public static void main(String[] args) {
		
		//Boolean - Logikai típus
		//csak igaz/hamis értéket vehet fel (true vagy false)
		//1 vagy 0
		
		boolean regisztralt = false; //deklaráció és definíció
		System.out.println(regisztralt);
		
		boolean eladva; //deklaráció: tipus és névmegadás
		eladva = true; //definició: értéket is kap
		System.out.println(eladva);
	}	

}
